public class menu {

    String dayname;
    String snacksimage;
    String snackscount;
    String teacount;

    public menu() {
    }

    public String getDayname() {
        return dayname;
    }

    public void setDayname(String dayname) {
        this.dayname = dayname;
    }

    public String getSnacksimage() {
        return snacksimage;
    }

    public void setSnacksimage(String snacksimage) {
        this.snacksimage = snacksimage;
    }

    public String getSnackscount() {
        return snackscount;
    }

    public void setSnackscount(String snackscount) {
        this.snackscount = snackscount;
    }

    public String getTeacount() {
        return teacount;
    }

    public void setTeacount(String teacount) {
        this.teacount = teacount;
    }
}
